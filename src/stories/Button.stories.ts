// 各ボタンのスタイル定義
const getButtonClass = (variant: string) => {
  switch (variant) {
    case 'primary':
      return 'add-button';
    case 'secondary':
      return 'action-button';
    case 'danger':
      return 'delete-button';
    case 'success':
      return 'rescan-directory-btn';
    case 'icon-only':
      return 'close-button';
    default:
      return 'add-button';
  }
};

